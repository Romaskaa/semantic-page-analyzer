from openai import OpenAI
from sklearn.metrics.pairwise import cosine_similarity
from sentence_transformers import SentenceTransformer
import numpy as np

client = OpenAI(base_url="https://openrouter.ai/api/v1", api_key="")

# Модель для embedding
embed_model = SentenceTransformer("sentence-transformers/all-MiniLM-L6-v2")

def retrieve_relevant_chunks(topic, knowledge_text, top_k=5, chunk_size=500):
    """
    Разбивает базу знаний на куски и выбирает наиболее релевантные по embedding
    """
    chunks = [knowledge_text[i:i+chunk_size] for i in range(0, len(knowledge_text), chunk_size)]
    topic_vec = embed_model.encode([topic])
    chunk_vecs = embed_model.encode(chunks)
    sims = cosine_similarity(topic_vec, chunk_vecs)[0]
    top_idx = sims.argsort()[-top_k:][::-1]
    return [chunks[i] for i in top_idx]

def generate_content_rag(topic, knowledge_text):
    relevant_chunks = retrieve_relevant_chunks(topic, knowledge_text)
    prompt = f"""
    Ты — профессиональный SEO-копирайтер и контент-маркетолог с 10-летним опытом. 
    Твоя задача — создать экспертную, глубокую и полностью готовую к публикации статью, 
    которая будет максимально полезна читателю и при этом идеально оптимизирована под поисковые системы.

    Тема статьи: 
    {topic}

    Исходные данные (база знаний):
    {" ".join(relevant_chunks)}

    Требования к статье:

    1.  **Структура и форматирование:**
        - **Заголовок H1:** Придумай цепляющий и кликбейтный (но без обмана), но при этом релевантный теме заголовок. Включи в него основное ключевое слово (например, "Как выбрать...", "Полное руководство по...", "Топ-10 лучших...").
        - **Вводная часть (Lead):** Напиши короткое вступление на 2-3 предложения, которое должно заинтриговать читателя, обрисовать проблему и пообещать ее решение в статье.
        - **Основная часть:** Логически разбей текст на смысловые блоки с подзаголовками H2 и, где необходимо, H3. Используй:
            - Маркированные и нумерованные списки для перечислений.
            - Выделение **жирным** ключевых моментов и терминов.
            - Таблицы, если нужно сравнить характеристики, цены или плюсы/минусы.
        - **Заключение:** Сделай краткие выводы по статье, резюмируя самое главное.

    2.  **SEO-оптимизация (Semantic Core):**
        - Используй основное ключевое слово из темы в H1, первом абзаце, в нескольких H2/H3 и равномерно по тексту.
        - Естественно впиши 3-5 дополнительных ключевых слов и фраз, близких к теме (LSI-запросы), чтобы охватить максимум смыслов. Не допускай "переспама" ключевыми словами.

    3.  **Стилистика и контент:**
        - Пиши экспертным, но понятным языком. Избегай сложных терминов без объяснений.
        - Стиль: информативный, дружелюбный, побуждающий к действию.
        - Опирайся исключительно на предоставленную "Базу знаний" ("relevant_chunks"). Не придумывай факты, которых там нет. Если в базе недостаточно информации для какого-то раздела, напиши "Информация по данному пункту требует дополнительного уточнения" и предложи логичный вариант развития темы.

    4.  **Дополнительные блоки (обязательно):**
        - **Часто задаваемые вопросы (FAQ):** Создай блок с 3-5 вопросами и краткими, четкими ответами на них. Вопросы должны касаться самых распространенных сомнений читателей по теме. Используй микроразметку (подразумевай структуру "вопрос-ответ").
        - **Блок советов / Лайфхаки:** (Опционально, но желательно) Добавь небольшой блок с практическими советами, которые помогут читателю избежать ошибок.

    5.  **Финальный результат:**
        - Предоставь статью в чистом, готовом к верстке виде.
        - В конце, после текста статьи, напиши небольшой блок **"Мета-данные (для редактора)"**, где укажи:
            - **Title (Заголовок для поисковиков):** до 60 символов.
            - **Description (Описание):** до 160 символов.
            - **Ключевые фразы (Keywords):** через запятую (основные 5-7 фраз).

    Статья должна быть объемной и закрывать потребность пользователя полностью (минимум 1500-2000 слов, если позволяет база знаний).
    """
    response = client.chat.completions.create(
        model="stepfun/step-3.5-flash:free",
        messages=[
            {"role": "system", "content": "Ты SEO эксперт"},
            {"role": "user", "content": prompt}
        ],
        extra_body={"reasoning": {"enabled": True}}
    )
    return response.choices[0].message.content